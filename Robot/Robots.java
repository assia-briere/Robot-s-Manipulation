package tp5robot;

public class Robots {

	private Casier casier;
	private Objet objet;
	private int nmcase;
	
	public Casier getCasier() {
		return casier;
	}
	public void setCasier(Casier casier) {
		this.casier = casier;
	}
	public Objet getObjet() {
		return objet;
	}
	public void setObjet(Objet objet) {
		this.objet = objet;
	}
	public int getNmcase() {
		return nmcase;
	}
	public void setNmcase(int nmcase) {
		this.nmcase = nmcase;
	}
	

}
